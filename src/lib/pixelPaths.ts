export function characterIconPath(character: string): string {
  switch (character) {
    case "Zephyr":
      return "/pixel/races/axion.svg";
    case "Korrath":
      return "/pixel/races/axion.svg";
    case "Vex":
      return "/pixel/races/axion.svg";
    case "Thane":
      return "/pixel/races/axion.svg";
    default:
      return "/pixel/races/axion.svg";
  }
}
